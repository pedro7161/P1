#include <stdio.h>
#include <stdlib.h>

// Programa REF_024_scanf_valor_retorno_3

// validação de entrada de dados num ciclo

// Este programa aplica a técnica de validação de dados para ler
// vários valores inteiros digitados somente terminando o input
// assim que seja digitado um tipo de dados diferente de numérico,
// como, por exemplo, um carater.

// O programa calcula também a soma dos dados inteiros digitados


int main()
{
	int num, i, els, soma, valor_retorno;
	
	els = 0;
	soma = 0;
	printf("\nDigite varios valores inteiros e para terminar digite uma letra: \n\n");
	
	while(scanf("%d", &num) == 1)
	{
		soma += num;

		els++;
	}

	printf("\n\nDigitou %d valores", els);
	printf("\n\nA sua soma = %d", soma);
	
	printf("\n\n");

    	system("PAUSE");	
	
	return 0;
}


