#include <stdio.h>
#include <stdlib.h>

// Programa REF_019_ciclos_4_for

// Aplicação do ciclo for
//
// O ciclo for é constituido por três partes
// Uma parte para inicialização das vars
// Uma parte para teste, a condição
// Uma parte para atualização das vars

// Exemplo: 
// 
// Calculo média de cinco temperaturas
//

#define NUM_TEMPS 5

int main()
{
	float temp, soma, media;
	int i;

	for(i = 0, soma = 0; i <  NUM_TEMPS; i++)
	{
		printf("\nDigite a proxima temperatura %d: ", i);
		fflush(stdin);
		scanf("%f", &temp);

		soma = soma + temp;	
	}

	media = soma / NUM_TEMPS;

	printf("\n\n A Media das temperaturas = %4.2f\n\n", media);
	system("PAUSE");	
  
    	return 0;

} // fecha o main

