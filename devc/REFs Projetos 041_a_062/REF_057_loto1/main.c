#include <stdio.h>
#include <stdlib.h>
#include <time.h>

#define N 5 // quantidade de números a sortear

//
// Programa REF_057_loto1

// Este programa cria cinco nums únicos a partir de n números 
// gerados aleatóriamente de totoloto

// Relembrar:

// 1. Que os nums não podem ser repetidos
// 2. O que implica termos que guardar os nums únicos num array
// 3. Os nums têm que estar no intervalo de valores entre 1 e 50

int main()
{
	int loto[N];

	int i; // contador de nums únicos

	int k; // var auxiliar na busca de nums repetidos

	int num;

// flag, reset pk ainda não saiu valor repetido

	int ja_saiu = 0; 

// inicializar os els do array a 0

	for (i = 0; i < N; i++)
	{
		loto[i] = 0;
	}

// gerar os nums aleatórios e guardar num array 
// os que não são repetidos

// reset i para servir de contador de nums únicos
// isto significa que i só vai ser actualizado no
// caso da ocorrência de um valor gerado ser único

	i = 0; 
	while (i < N)
	{
// gerar num aleatório

		srand((unsigned)time(NULL)); // a semente para realimentar rand
		num = 1 + ( rand() % 50 ); // gera num aleatório
		 	
		ja_saiu = 0; // reset flag  para assinalar que 
			
// mas temos que verificar se realmente o novo num ainda nao saiu
// percorremos o array até ao último elemento inserido num ainda não saiu
				
		for (k = 0; k < i; k++)
		{
			if(num == loto[k]) 
			{
				ja_saiu = 1; // num gerado já existe no array
			}

		} // fecha for


		if (ja_saiu == 0)
		{

// então temos um num gerado não repetido

			loto[i] = num; // insere novo num único

			i++;  // actualiza ctdr nums únicos
		}

	} // fecha while

// Ver os nums da sorte

	printf("\n\n\n Eis os numeros sorteados: \n\n");
	for (i = 0; i < N; i++)
	{
		printf("\n%d", loto[i]);
	}

	printf("\n\n");
	system("pause");

	return 0;
}

