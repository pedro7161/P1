#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// Programa REF_055_copia_strings_e_aloc_din_mem_1

// Cópia de strings e introdução à alocação dinâmica de Memória

// Cópia de strings, aplicando em revisão a primitiva strcpy()
//
// e também aplica alocação dinâmica de Memoria
// na manipulação de cópias de strings (sem uso 
// da primitiva strcpy)
// Ou seja, vamos simular o que strcpy() faz.
//

int main(int argc, char *argv[])
{
// cópia de strings com o comando strcpy() - em revisão - 
// sem alocação dinâmica de memória

	char fonte[30] = "lisboa";
	char destino[30];

	strcpy(destino, fonte);

	printf("\n\n strcpy: Sem alocacao dinamica de Memoria\n");
	printf("\n Fonte: %s", fonte);
	printf("\n\n Copia: %s", destino);

// cópia de strings com o comando strcpy() com alocação 
// dinâmica de memória

	printf("\n\n strcpy: Com alocacao dinamica de Memoria\n");

// o comando abaixo declara uma string através de ptrs
// faz o mesmo que:
//
// char orig[30];
//
	char *orig = "janeiro"; 

// alocar dinamicamente espaço de M para o destino

// o comando malloc obtém uma área de M livre para nosso uso
// devolve o endereço da primeira posicao da area de M
// este endereço deve ser atribuido a um ptr do tipo de dados
// que vai conter a area de M que nos foi concedida (alocada)
//
// no comando malloc temos tabém que indicar para que tipo 
// de dados pretendemos utilizar essa area
//

	char *dest = (char *) malloc (30);

	if (dest == NULL) printf("\nNao Ha M livre");
	
	strcpy(dest, orig);

	printf("\n\n"); 
	puts(orig);
	puts(dest);

//
//
// repetir o processo anterior mas simulando a func 
// strcpy
// ou seja, vamos percorrer a memória
// sendo assim, nunca podemos perder de vista 
// o inicio da area livre e da memória apontada.
//
// na execução anterior as primitivas strcpy e puts
// mantiveram os inicios intactos das memorias
//
	char *inicioDest = dest; // guarda endereco inicial de dest
	char *inicioOrig = orig; 

/*
	while (*orig)
	{
		*dest++ = *orig++;
	}
*/
// o ciclo acima pode ser substituído pelo
// pela simples linha de código;

	while(*dest++ = *orig++);


// reset ptrs para o inicio

	orig = inicioOrig;
	dest = inicioDest;

	puts(orig);
	puts(dest);
	
	printf("\n\n");
	system("pause");

	return 0;
}

