#include <stdio.h>
#include <stdlib.h>

// Programa REF_056_macros

// As macros são partes de código implementadas, no inicio 
// do programa, logo a seguir às declarações dos #includes.

// Assemelham-se a funções, mas têm uma natureza completamente
// diferente.

// As macros são processadas pelo pre-processador
// O Pre-processador é um programa que processa o código
// antes de chegar ao compilador. 
// Assim o código das macros sao muito elementares, nao têm o
// conhecimento de tipos de dados, limitam-se assim a fazer 
// substituições.
//
// Uma macro é definida através do comando #define
// As macros funcionam através de um processo de substituição,
// onde um identificador num programa é substituído por uma string
// pré definida.
//
// A sintaxe é a seguinte:
//
// #define identificador string
//
// O preprocessador substitui toda a ocorrência do identificador
// no código pela string.
//
// Exemplo:
//
// #define CUBO(x) (x * x * x)
//
// o qual resultará depois de uma chamada do tipo:
//
//	volume = CUBO(lado);
// 
// na substitução em:
//
// volume = (lado * lado * lado)
//
// Na definição da macro não terminar com ; porque daria erro
// já que ; iria fazer parte da substituição

#define MAX 100
#define CUBO(x) (x * x * x)

void main()
{
	int x = MAX;
	printf("\n\nO Valor maximo = %d", x);

	int lado = 2;
	int volume;

	volume = CUBO(lado);

	printf("\n\nO volume = %d", volume);

	printf("\n\n\n");
	system("pause");

	return;
}
