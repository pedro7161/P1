{
	float m;

	m = ((float)a + (float)b) / 2;

	return m;

} // fecha calc2

// Função para imprimir um carater n vezes 
void escreve(int n, char ch)
{
	int i;

	i = 0;
	while (i < n)
	{
	  printf ("%c", ch);

	  i++;
	}
} // fecha escreve


