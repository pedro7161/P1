// Função para somar e devolver a soma de dois inteiros
int calc1(int num1, int num2)
{
	int soma;

	soma = num1 + num2;

	return  soma;

} // fecha calc1

// Função para calcular e devolver a média 
// de dois inteiros (atenção média é float)
