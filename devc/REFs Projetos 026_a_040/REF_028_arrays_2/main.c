/*
Revendo os arrays:

Um array é uma variável indexada que normalmente apresenta-se
sob as formas uni-dimensionais e bi-dimensionais.

Um array uni-dimensional é também conhecido como vetor.

Por sua vez, um array ou vetor bi-dimensional é também conhecido por 
matriz.

Os arrays são considerados como as estruturas de dados mais simples.

Essencialmente, destinam-se a manter uma série de elementos de dados,
os quais devem ser do mesmo tamanho e tipo de dados. 

Os seus elementos individuais são acedidos através do nome do vetor 
a que pertencem e por um índice de posição,também chamado de subscrição,
dentro de parênteses rectos.

Por exemplo, no caso dos arrays unidimensionais, nums[0] refere-se ao 
elemento 0 do array chamado nums.

Por exemplo, no caso dos arrays bi-dimensionais, tabela[2][1] refere-se
ao elemento 2,1 (a interseção da linha 2, que é a terceira linha, já que 
a primeira linha é a linha 0, com a coluna 1, que é a segunda coluna, já 
que a primeira coluna é a coluna 0) do array bidimensional chamado tabela.
 
O índice geralmente utiliza uma sequência de números inteiros,(ao contrário 
de um array associativo) mas o índice pode ter qualquer valor ordinal.
 
Os arrays têm a vantagem de que os seus elementos são acessíveis de forma 
rápida mas têm a limitação dos seus dados terem que ser de tamanho fixo e 
do mesmo tipo de dados.

A forma de aceder aos elementos de um array é assim directa, ao contrário 
das listas, conforme constataremos brevemente.

Isto quer dizer que o elemento desejado obtem-se a partir do seu índice e 
não é preciso procurá-lo elemento por elemento. 

No caso das listas, por exemplo, para alcançar o quarto elemento ter-se-á 
de aceder primeiro aos três anteriores, a não ser que se guarde um ponteiro 
que permita aceder de maneira rápida a esse elemento em particular, também 
conforme constataremos brevemente.

Arrays unidimensionais:

Exemplos:

 
Declaração:

double temperaturas[5];
	
Declara um array chamado temperaturas do tipo double de 5 elemementos


Declaração e inicialização:

int vals[] = {10, 20, 30, 15, 25};

ou :

int vals[5];

vals[0] = 10;
vals[1] = 20;
vals[2] = 30;
vals[3] = 15;
vals[4] = 25;

*/

//
// Cálculo da media de cinco temperaturas digitadas
// Cálculo do valor máximo
// Cálculo do valor mínimo

#define NUM_TEMPS 5

int main()
{
	float temps[NUM_TEMPS];
	float soma, media, maximo, minimo;
	int i;

// carregar o array
	for (i = 0; i < NUM_TEMPS; i++)
	{
		printf("\nDigite a temperatura %d: ", i);
		fflush(stdin);	
		scanf("%f", &temps[i]);
	}

// visualizar os dados introduzidos
	printf("\nEis os valores digitados:\n\n ");
	for (i = 0; i < NUM_TEMPS; i++)
	{
		printf("\n%4.2f", temps[i]);
	}

// calcular a media
	for (i = 0, soma = 0; i < NUM_TEMPS; i++)
	{
		soma = soma + temps[i];
	}

	media = soma / NUM_TEMPS;

	printf("\n\nA Media = %4.2f", media);

// calculo valor maximo

	maximo = temps[0]; // supor q valor max é este
	for (i = 1; i < NUM_TEMPS; i++)
	{
		if (temps[i] > maximo)

			maximo = temps[i];
	}
	printf("\n\nO valor Maximo = %4.2f", maximo);

	minimo = temps[0]; // supor q valor max é este
	for (i = 1; i < NUM_TEMPS; i++)
	{
		if (temps[i] < minimo)

			minimo = temps[i];
	}

	printf("\n\nO valor Minimo = %4.2f", minimo);


	printf("\n\nDigite um carater para continuar\n\n");
	fflush(stdin);	
	getchar();

	return 0;
} // fecha o main

